
public class Loop {
	
	public static void main(String[] args) {
		
		int [] numbers = {10,20,30,40};
		
		for (int x : numbers) {
			
			System.out.print(x);
			System.out.print(",");
			
		}
		
		String [] names = {"yamini","raghu","aadhya"};
		
		for(String s : names) {
			System.out.print("\n");
			System.out.print(s);
		}
	}
	
	
	
}
